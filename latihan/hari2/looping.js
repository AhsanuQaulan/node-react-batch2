// for loop
for (var angka = 1; angka < 10; angka++) {
  console.log("Iterasi ke- " + angka);
}
console.log("");

// while loop
var flag = 1;
while (flag < 10) {
  console.log("Iterasi ke-" + flag);
  flag++;
}
console.log("");

var flag = 9;
do {
  console.log("Iterasi ke-" + flag);
  flag--;
} while (flag > 0);
